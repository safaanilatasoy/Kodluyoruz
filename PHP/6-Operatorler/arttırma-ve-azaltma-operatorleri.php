<?php

// Arttırma Operatörü ----> ++
$a = 5;
$a++; // $a = $a + 1;
echo $a; // Sonuç: 6

## Sonradan Arttırma Operatörü ----> $a++
$a = 5;
echo $a++; // Sonuç: 5
echo $a; // Sonuç: 6

## Önceden Arttırma Operatörü ----> ++$a
$a = 5;
echo ++$a; // Sonuç: 6
echo $a; // Sonuç: 6


// Azaltma Operatörü ----> --
$a = 5;
$a--; // $a = $a - 1;
echo $a; // Sonuç: 4

## Sonradan Azaltma Operatörü ----> $a--
$a = 5;
echo $a--; // Sonuç: 5
echo $a; // Sonuç: 4

## Önceden Azaltma Operatörü ----> --$a
$a = 5;
echo --$a; // Sonuç: 4
echo $a; // Sonuç: 4



?>